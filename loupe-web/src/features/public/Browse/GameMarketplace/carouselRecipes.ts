// ──────────────────────────────────────────────────────────────────────────
// Seeded rotation — the "fresh every visit" selection layer.
// ──────────────────────────────────────────────────────────────────────────

/** Tiny deterministic PRNG (mulberry32) so a given seed always shuffles the
 *  same way — stable within a visit, varied across visits. */
function rng(seed: number): () => number {
  let s = seed >>> 0;
  return () => {
    s |= 0;
    s = (s + 0x6d2b79f5) | 0;
    let t = Math.imul(s ^ (s >>> 15), 1 | s);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffle<T>(arr: readonly T[], seed: number): T[] {
  const out = [...arr];
  const rand = rng(seed);
  for (let i = out.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [out[i], out[j]] = [out[j]!, out[i]!];
  }
  return out;
}

/**
 * Compose the ordered rails for a game: stable "anchor" shelves plus a
 * seed-shuffled sample of the strategy pool, so the storefront varies per
 * visit like a real marketplace. `aiRecipes` (from the AI generator) are mixed
 * into the rotating pool when present.
 */
export function composeGameRails(
  label: string,
  seed: number,
  opts: { aiRecipes?: CarouselRecipe[]; rotatingCount?: number } = {},
): RailSpec[] {
  const rotatingCount = opts.rotatingCount ?? 5;

  // Anchor: a momentum shelf that always leads (priced games show real
  // trending; catalog-only games self-hide it and fall through to the rest).
  const anchorTop: RailSpec = {
    kind: "cards",
    id: "trending",
    title: `Trending in ${label}`,
    subtitle: `The ${label} cards moving most across connected marketplaces.`,
    fetch: { sort: "trending", limit: 24 },
    minItems: 4,
  };

  // Rotating middle: a varied sample of curated + AI strategies.
  const pool = [...CURATED_RECIPES, ...(opts.aiRecipes ?? [])];
  const picked = shuffle(pool, seed)
    .slice(0, rotatingCount)
    .map((r) => recipeToRailSpec(r, label));

  // Anchors that close every storefront.
  const explore: RailSpec = {
    kind: "catalog",
    id: "explore",
    title: `Explore ${label}`,
    subtitle: `A look across the ${label} catalog — open any card for details.`,
    sort: "newest",
    limit: 20,
    minItems: 4,
  };
  const sets: RailSpec = {
    kind: "sets",
    id: "sets",
    title: `Shop ${label} sets`,
    subtitle: `Browse ${label} by set — every release, with live card counts.`,
    minItems: 1,
  };
  const sealed: RailSpec = {
    kind: "sealed",
    id: "sealed",
    title: `Sealed ${label} products`,
    subtitle: "Booster boxes, ETBs, and bundles — tracked like singles.",
    minItems: 1,
  };

  return [anchorTop, ...picked, explore, sets, sealed];
}
