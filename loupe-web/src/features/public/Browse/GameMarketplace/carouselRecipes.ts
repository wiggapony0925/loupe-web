/**
 * carouselRecipes — the merchandising engine behind the marketplace.
 *
 * A **recipe** is a fully JSON-serializable carousel definition: a title plus a
 * constrained filter vocabulary (price band, rarity, sort). This is exactly how
 * large storefronts (Amazon et al.) work — they don't hand-write every shelf;
 * they keep a *pool of strategies* and a selection layer samples a varied subset
 * per visit, so the page feels fresh ("different data sets every time"). Because
 * a recipe is pure data over a fixed vocabulary, the SAME contract can be:
 *   1. authored by hand (the curated pool below),
 *   2. generated by an LLM (see `/v1/public/carousels` — AI picks creative
 *      themes + copy + the filter recipe, never the data),
 * and then compiled to a real `RailSpec` the existing `MarketplaceRail` renders.
 *
 * The compiler maps a recipe to the rail kinds we already have, so nothing new
 * is needed downstream — an AI-invented "Rainbow rares under $100" shelf renders
 * through the same path as the curated rails.
 */
import type { CarouselRecipe } from "@loupe/core";
import {
  priceOver,
  priceUnder,
  priceBetween,
  rarityMatches,
  inRarities,
  byPriceDesc,
  byPriceAsc,
  byName,
  type CardPredicate,
  type CardComparator,
} from "./cardFilters";
import type { RailSpec } from "./railCatalog";

// `CarouselRecipe` (the serializable carousel contract — what the AI generator
// emits and the curated pool is written in) lives in @loupe/core so the web and
// the API share one definition.

const COMPARATORS: Record<string, CardComparator> = {
  price_desc: byPriceDesc,
  price_asc: byPriceAsc,
  name: byName,
};

const interp = (s: string, label: string) => s.replaceAll("{label}", label);

/** Compile a serializable recipe into a real `RailSpec` for `MarketplaceRail`. */
export function recipeToRailSpec(r: CarouselRecipe, label: string): RailSpec {
  const title = interp(r.title, label);
  const subtitle = interp(r.subtitle, label);
  const minItems = r.minItems ?? 4;

  if (r.source === "catalog") {
    return {
      kind: "catalog",
      id: r.id,
      title,
      subtitle,
      sort: r.sort === "name" ? "name" : "newest",
      limit: r.limit ?? 20,
      minItems,
    };
  }

  const where: CardPredicate[] = [];
  if (r.priceMin != null && r.priceMax != null)
    where.push(priceBetween(r.priceMin, r.priceMax));
  else if (r.priceMin != null) where.push(priceOver(r.priceMin));
  else if (r.priceMax != null) where.push(priceUnder(r.priceMax));
  if (r.rarityPattern) where.push(rarityMatches(new RegExp(r.rarityPattern, "i")));
  if (r.rarities?.length) where.push(inRarities(...r.rarities));

  return {
    kind: "cards",
    id: r.id,
    title,
    subtitle,
    fetch: {
      sort: r.source === "trending" ? "trending" : "value",
      maxPrice: r.priceMax,
      limit: 24,
    },
    lens: {
      where: where.length ? where : undefined,
      sort: COMPARATORS[r.sort ?? "price_desc"],
      limit: r.limit ?? 20,
    },
    minItems,
  };
}

